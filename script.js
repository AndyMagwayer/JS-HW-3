var загаданное_число = Math.floor(Math.random() * 10) + 1;

var ответ = prompt("Попробуйте угадать число от 1 до 10:");

if (ответ == загаданное_число) {
  // Если ответ верный
  window.open("https://itlabs.uz", "_blank"); // Открыть новую вкладку с указанной ссылкой
} else {
  // Если ответ неверный
  window.close(); // Закрыть текущее окно
}